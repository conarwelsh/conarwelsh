	<p>I have a genuine fascination over the entire web stack. &nbsp;Each layer is so very complimentary to the rest, and a working knowledge of all them allows for a more organized and scalable infrastructure. &nbsp;I am frequently asked which part of the stack is my favorite, but I think my favorite part is actually the mix of them, and seeing how they all intertwine.</p>

	<p>My most valuable skill by far is my ability to learn <em>new abilities</em>.  In fact, my favorite part of this industry is that you can never <em>really</em> be a "master" of it because it is never done changing.  There is always something new to learn, if you find yourself bored in this industry the only thing lacking is your own ambition, because there are no boundaries.</p>

	<p>Currently, my favorite kind of project is to build a responsive, single page app in Angular, that is powered by a RESTful Laravel API. &nbsp;Especially if it is a real-time applications using web sockets, and attempting to re-create more of a native-app feeling for the user.</p>
